def find_swift_files(root: Path, target_dirs: List[str]) -> List[Path]:
    """ì§€ì •ëœ ë””ë ‰í† ë¦¬ ë‚´ ëª¨ë“  .swift íŒŒì¼ì„ ê²€ìƒ‰í•©ë‹ˆë‹¤."""
    print("ğŸ” Swift íŒŒì¼ì„ ê²€ìƒ‰í•©ë‹ˆë‹¤...")
    swift_files = []
    for dir_name in target_dirs:
        search_path = root / dir_name
